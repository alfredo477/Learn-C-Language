//二维数组是相同类型元素按行和列排列的矩形集合
//二维数组是把一维数组作为元素的数组
#include<stdio.h>
int main(){
    int arr1[2][3]={1,2,3,4,5,6};//完全初始化
    //指定行和列初始化
    int arr2[2][3]={{1,2,3},{4,5,6}};
    int arr3[2][3]={{1,2,3},{4}};//第二行元素不足 补0
    int arr4[][3]={1,2,3,4,5,6};//可以省略行 不可省略列
    printf("%d\n",arr1[0][2]);
    printf("%d\n",arr1[1][0]);
    printf("%d\n",arr1[1][2]);
}